magicians = ['Harry Houdini', 'David Blaine', 'Criss Angel']
def show_magicians(mages):
    for mage in mages:
        print(mage)

def make_great(mages):
    for i in range(len(mages)):
        mages[i] = "the Great " + mages[i]

make_great(magicians)
show_magicians(magicians)


